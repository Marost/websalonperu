            <p> PERSECUCIÓN A MIGRANTES</p>
    </div>
                    <div id="contenido_info_centro">
                      <div id="texto_cotenido2">
                   	    <p>En varias ocasiones hemos tratado sobre los movimientos migratorios, los padecimientos de los migrantes en búsqueda de oportunidades laborales y mejores condiciones de vida, así como el compromiso de estos últimos con el país de origen y con la familia que dejaron.</p>
                   	    <p>&nbsp;</p>
                   	    <p>Hay migrantes que tienen sus papeles en orden, esto es autorización para trabajar y residencia en los países que los albergan, pero hay otros que no los tienen, aunque se las agenciaban para vivir sin ellos.   La situación de estos últimos ha variado en muchos países, como por ejemplo en Italia al haberse dictado severas disposiciones legales en materia de extranjería, como también en  Japón y en los Estados Unidos de América en que su situación se ha tornado crítica, entre otros países.</p>
                   	    <p>&nbsp;</p>
                   	    <p>En efecto,  recordemos que en el Estado de Arizona en breves días entra en vigor la Ley SB 1070, la cual penaliza a los inmigrantes carentes de permiso para laborar y residir, y que incluso pueden ser pasibles de detención por autoridades migratorias y policiales por la simple sospecha de su condición de indocumentados, lo que en buena cuenta se podrá ejecutar por los rasgos raciales de  aquellos.</p>
                   	    <p>Lejos de haberse revertido los peligros para los migrantes indocumentados de Arizona, el mal ejemplo se expande, tan es así que en el Estado de Nebraska, específicamente en la ciudad de Fremont ha sido  prohibido el alquiler de inmuebles a migrantes sin documentación, así como también el darles trabajo.</p>
                   	    <p>&nbsp;</p>
                        <p>En otras circunscripciones territoriales de los Estados Unidos de América, tales como Florida, Idaho y  Minnesota se vienen proponiendo medidas tan absurdas como las de Arizona, lo que está llevando a la desesperanza a muchísimas familias migrantes, que perciben los peligros de su permanencia en los lugares en que viven y laboran, y ven que las promesas electorales  que se hicieron para lograr el voto de los originarios de Latinoamérica en el último proceso electoral, no tienen hasta ahora correlato con la realidad. Sin embargo se abre una ventana de esperanza con la reciente invocación del Presidente Obama para que se apruebe en su país la reforma migratoria que termine con la angustia de los indocumentados.</p>
                        <p>&nbsp;</p>
                   	    <p>Estamos frente a la persecución de migrantes indocumentados bajo el tamiz de regulaciones de extranjería, que constituye acción discriminatoria por razón étnica, lo que es contrario al artículo séptimo de la Declaración Universal de los Derechos Humanos.  </p>
                   	    <p>&nbsp;</p>
                   	    <p>También se transgrede, con el accionar discriminatorio que hemos mencionado, la prohibición de expulsión colectiva de extranjeros que norma el inciso 9 del art. 22 de la Convención Americana sobre Derechos Humanos, pese a que la traten de disimular con decisiones singulares.  Si la persecución es por sospecha, la sospecha por rasgos étnicos, y la expulsión es de todos aquellos de rasgos diferentes a los arios encontrados sin documentación debida, ergo la expulsión es colectiva por más maquillaje legal que se aplique.</p>
                   	    <p>&nbsp;</p>
                   	    <p>Su Santidad Benedicto XVI en su Carta Encíclica &ldquo;Caritas in Veritate&rdquo;,  respecto a las migraciones, propicia una política de cooperación internacional a la que hay que acompañar de &ldquo;…adecuadas normativas internacionales capaces de armonizar los diversos ordenamientos legislativos, con vista a salvaguardar las exigencias y los derechos de las personas y de las familias emigrantes, así como las de las sociedades de destino&rdquo;.  Les reconoce a los migrantes la contribución &ldquo;…con su trabajo al desarrollo económico del país que los acoge, así como a su país de origen a través de las remesas de dinero&rdquo;, pero lo que es más importante, enfatiza que  &ldquo;todo migrante es una persona humana que… posee derechos fundamentales inalienables que han de ser respetados por todos y en cualquier situación&rdquo;.</p>
                   	    <p>&nbsp;</p>
                   	    <p>¿Y qué hacen ante ello las Naciones Unidas y la Organización de Estados Americanos?  Lamentablemente la respuesta es: muy poco, pues más allá de ponposas declaraciones y resoluciones, su valor no va más allá de papel mojado, por lo que se hace imperativo que cumplan con el rol de defensa de los Derechos Humanos para los que fueron creadas, esperando que también las Organizaciones No Gubernamentales  -que se supone defienden Derechos Humanos-  hagan lo suyo, que en este caso es más que necesario.</p>
						<div id="texto_cotenido2">
							<p> <strong>Por Antero Flores-Araoz E.</strong></p>
