        <div id="texto">
            <p> CONGRESO Y QUINTO SUYO </p>
        </div>
                    <div id="contenido_info_centro">
                      <div id="texto_cotenido2">
                   	    <p>Nuestros compatriotas que migraron al extranjero, motivados principalmente en la búsqueda de oportunidades laborales, desde hace muchos años  reclaman elegir sus propios representantes al Congreso del Perú.</p>
                   	    <p>Como hasta ahora nuestros compatriotas aludidos son considerados dentro del Distrito Electoral de Lima, únicamente pueden elegir como congresistas a los que postulan a representar a Lima, quedando vulnerada su aspiración de elegir a sus propios representantes.</p>
                   	    <p>&nbsp;</p>
                   	    <p>En varias ocasiones se han presentado proyectos de ley para crear el Distrito Electoral de Peruanos en el Exterior, pero lamentablemente siempre se han estrellado con dos pretextos.  El primero de ellos es que la Constitución señala número fijo de parlamentarios, y crear el Distrito Electoral de Peruanos en el Exterior, significaría reducir el número de representantes de otros distritos electorales para dar cabida a los del nuevo distrito.  La segunda excusa es que elevar el número de representantes al Congreso significará mayores gastos al Estado.</p>
                   	    <p>&nbsp;</p>
                   	    <p>En lo que se refiere al número fijo de parlamentarios, si bien ello es cierto al haber establecido  originalmente la Constitución de 1993 la cifra de 120 congresistas, tal reticencia ya fue vencida el pasado año cuando por la reforma constitucional según Ley 29402 se elevó el número a 130, con la finalidad de que los ciudadanos de las provincias del Departamento de Lima tuvieran su propio distrito  electoral distinto al de Lima Metropolitana, como en efecto hoy lo tienen al haberse también modificado la Ley Orgánica de Elecciones mediante Ley 29403.</p>
                   	    <p>&nbsp;</p>
                   	    <p>Si ya se realizó la tarea para que en justicia los ciudadanos del resto de provincias de Lima tengan sus propios representantes al Congreso, no habría motivo valedero alguno para no realizar un segundo esfuerzo normativo -constitucional y legal- para que nuestros hermanos del llamado simbólicamente &ldquo;Quinto Suyo&rdquo; tengan también el derecho a elegir a sus propios congresistas.</p>
<p>&nbsp;</p>
<p>En cuanto al pretexto de que ello significaría mayores egresos del Presupuesto General de la República, bien podría legislarse que no demandará más gastos, estableciéndose como tope de egresos en el Poder Legislativo el que actualmente se tiene, sin olvidar que se pueden hacer ajustes y racionalización del gasto.</p>
                   	    <p>&nbsp;</p>
                   	    <p>La excusa del mayor gasto también se ha argumentado como negativa para volver al Sistema Bicameral restableciéndose el Senado, pero incluso con Senado no habría motivo para incurrir en mayores egresos si hay una eficiente racionalización de egresos.</p>
                   	    <p>&nbsp;</p>
                   	    <p>En efecto, no se requiere de dos administraciones diferenciadas entre Cámara de Diputados o de Representantes y Senado de la República, basta con una sola administración en que únicamente se necesitaría duplicar funciones en Oficialía Mayor para cada Cámara y los relatores de las mismas.  En cuanto personal hay más que suficiente y no olvidemos que hasta  hace pocos lustros nuestros parlamentarios no tenían oficinas individuales ni contaban con personal directo y de confianza a su servicio.  Recordemos que hoy cada uno tiene siete personas que lo asisten, y no me digan que no se podría redistribuir tal fuerza laboral entre diputados y senadores.</p>
                   	    <p>&nbsp;</p>
                   	    <p>Antaño los parlamentarios eran numéricamente más, no tenían las facilidades logísticas y de personal que hoy tienen, pero sin embargo el desempeño en la función era muy superior, los dictámenes de mayor valor conceptual y los debates de un nivel superior al actual.</p>
                   	    <p>&nbsp;</p>
                   	    <p>No más excusas, necesitamos el retorno del Senado, sin duplicar las funciones de la actual Cámara, pero también que nuestros compatriotas del Quinto Suyo puedan elegir a sus propios representantes.  Eso sí, si se procede al debate de la restauración del Senado que sea en cuerda separada de otras reformas constitucionales que se han venido proponiendo como la renovación del Parlamento o la revocación del mandato congresal, o la del voto facultativo.  Son temas diferentes, que deberían ser tratados independientemente y no en un mismo fardo.</p>
<div id="texto_cotenido2">
                <p> <strong>Por Antero Flores-Araoz E.</strong></p>
