            <p>NOTA DE PRENSA:
            <p><span class="texto-12marron-Arial">IV CONVENCIÓN MUNDIAL FEMIP<br />
Barcelona, 21 - 23 de Octubre 2011             </span>            
          <span class="texto-12marron-Arial"> Sala de Convenciones del HOTEL MELIÁ BARCELONA </span></div>
                    <div id="contenido_info_centro">
                      <div id="texto_cotenido2">
                   	    <p>Del 21 al 23 de Octubre se celebrará en  Barcelona la IV Convención Mundial de la Federación Mundial de Instituciones  Peruanas - FEMIP, organizada por Salón Perú A.C.</p>
                   	    <p>&nbsp;</p>
                        <p>La   parte más importante de la IV Convención Mundial son las conferencias,  que este año, se centrarán en la protección ambiental y  los &ldquo;Bonos de Carbono&rdquo; que es un mecanismo  internacional de descontaminación para reducir las emisiones contaminantes al  medio ambiente. Aplicando este mecanismo aprobado en el Protocolo de Kioto,  contribuiremos a la descontaminación de la Amazonía peruana; pero a su vez  potenciaremos un nuevo mercado emergente basado en el llamado Mecanismo de Desarrollo  Limpio (MSL) que atraerá nuevos inversionistas y con ellos ofertas y demandas  en beneficio de la población peruana. El estado peruano al no desarrollar  las recomendaciones de Kioto deja de percibir  más de 500 millones de dólares al año al no cumplir con el acuerdo que tiene  con Brasil para la protección de la Amazonía peruana.</p>
                        <p>&nbsp;</p>
                        <p>Otra parte importante de las  conferencias estarán orientadas a tratar  temas de interés de la comunidad peruana residente en el exterior. Entre ellas  la vida institucional de los peruanos y la promoción del Perú en los países de  residencia; problemática relacionada con los servicios consulares; las leyes de  incentivo al retorno y la necesidad de una representación parlamentaria de los  tres millones de  peruanos que residen en  el exterior.</p>
                        <p>&nbsp;</p>
                        <p>Adicionalmente tendremos Conferencias  Magistrales a cargo de destacados y reconocidos profesionales del ámbito  académico, político, diplomático y empresarial.</p>
                        <p>&nbsp;</p>
                        <p>&ldquo;Rostros del Perú&rdquo; es la exposición  fotográfica que da inicio a nuestra Convención, de la destacada poeta y  fotógrafa peruano-francesa Monique Pineda.  Esta exposición será una pequeña muestra a través de las imágenes de la inmensa  riqueza cultural  del Perú  y una expresión  viva de su   diversidad.</p>
                        <p>&nbsp;</p>
                        <p>Los   delegados que asistirán a Ia IV Convención Mundial son peruanos  residentes en España, EEUU, Canadá, Suecia, Italia y Japón. Entre los  asistentes y expositores  tendremos la  visita especial del actual Ministro en la Cartera de Defensa Don Daniel Mora  Zevallos, así como de un destacado grupo de empresarios peruanos y miembros del  Cuerpo Diplomático que representan al Perú ante la O.E.A, UNICEF y Embajadas  del Perú en Europa; entre ellos dos Ex Ministros, del Ministerio de Relaciones  Exteriores del Perú; el Embajador Manuel Rodríguez Cuadros y el Embajador Oscar  Maúrtua de Romaña; y, el Embajador Hernán Couturier Mariátegui, actual  Embajador del Perú en el Reino Unido de Gran  Bretaña e Irlanda del Norte.</p>
                        <p>&nbsp;</p>
                        <p>El día de la Inauguración, en la parte  cultural mostraremos en Barcelona, el enorme potencial artístico del Perú a  través de sus danzas e interpretaciones. La comida de confraternidad permitirá  premiar y reconocer la labor de las instituciones peruanas en favor de la  comunidad residente en el exterior así como de su enorme contribución en la  promoción del Perú en el mundo. </p>
                        <p>&nbsp;</p>
                        <p>Para finalizar, se realizará una Cena  de Gala que hará de esta IV Convención Mundial un buen pretexto para vivir el  Perú en Barcelona y conocer la calidez y profesionalidad de su gente.</p>
Acepta nuestra cordial Invitación.   ¡Ven a vivir el Perú con nosotros!
