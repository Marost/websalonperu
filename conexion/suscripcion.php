<div id="contenido">
<p><strong>Bienvenidos a  &lsquo;Salón Perú&rsquo; 2010:<br />
  </strong><br />
  Reciban una invitación  cordial para participar en la VI  edición de nuestro encuentro comercial: &lsquo;Salón Perú&rsquo;, que se llevará a cabo el  próximo 22, 23 y 24 de octubre de 2010 en la Fira de Barcelona, espacio en el que año con año  se celebran las ferias internacionales con más relevancia del mundo.</p>
<p><br />
  &lsquo;Salón Perú&rsquo; se ha consolidado  como una gran e importante vitrina del Perú en Europa. Es en concreto, un  camino para la proyección comercial del pujante sector textil, agroalimentario,  inmobiliario, gastronómico, artesanal, y de turismo rural peruano, que utiliza  estratégicamente Cataluña como punto de encuentro, por su grado de proyección  internacional.</p>
<p>&nbsp;</p>
<p>La capital catalana es la quinta ciudad más próspera del mundo,  cosmopolita y abierta. En ella, más precisamente en la Fira de Barcelona (Feria de  Barcelona),  se dan cita eventos  internacionales tales como pasarela Gaudí, el &nbsp;referente más importante de  la moda en España, o el Mobile World Congress, el estandarte de la tecnología  móvil a nivel global.  </p>
<p>&nbsp;</p>
<p>Más  de 800 empresas participantes de Perú, Italia, Francia, EEUU, Canadá, y España,  junto más de 60.000 visitantes, ha sido parte del éxito que ha cosechado  durante cinco años &lsquo;Salón Perú&rsquo;, un resultado que estamos seguros continuará en  2010.</p>
<p>&nbsp;</p>
<p>Como en ediciones pasadas, este año ofreceremos también un  completo programa académico. El curso estará orientado a las energías  renovables, por ello hacemos extensiva nuestra invitación a todos los agentes  sociales (autoridades regionales y &nbsp;municipales, partidos políticos y  &nbsp;empresarios) comprometidos con el desarrollo del país.</p>
<p>&nbsp;</p>
<p>Una vez más &lsquo;Salón Perú&rsquo; será nuestro punto de encuentro, para  vivir contigo, &nbsp;el Perú que todos queremos, para tener la opción de  generar además nuevos caminos para grandes negocios.</p>
